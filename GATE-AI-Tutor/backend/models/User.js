import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
      select: false,
    },
    role: {
      type: String,
      enum: ["superadmin", "admin", "employee", "intern", "client"],
      default: "employee",
    },
  },
  { timestamps: true }
);

export const User = mongoose.model("User", userSchema);
